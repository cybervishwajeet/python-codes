# Example program demonstrating the use of looping statements

# Using the continue statement
for i in range(1, 6):
    if i == 3:
        continue
    print(i)

# Using the pass statement
for i in range(1, 6):
    if i == 3:
        pass
    print(i)

# Using the break statement
for i in range(1, 6):
    if i == 3:
        break
    print(i)