# while loop
count = 0
while count < 5:
    print("While loop iteration:", count)
    count += 1

# for loop
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("Fruit:", fruit)

# nested loops
for i in range(1, 4):
    for j in range(1, 4):
        print(i, "*", j, "=", i * j)