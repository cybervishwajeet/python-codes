# if statement
x = 10
if x > 0:
    print("x is positive")

# if...else statement
y = -5
if y > 0:
    print("y is positive")
else:
    print("y is negative")

# nested if statement
z = 0
if z > 0:
    print("z is positive")
elif z < 0:
    print("z is negative")
else:
    print("z is zero")