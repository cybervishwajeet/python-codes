import math

num = 16
log = math.log(num)
sin = math.sin(num)
cos = math.cos(num)
sqrt = math.sqrt(num)

print("The square root of", num, "is", sqrt)
print("The logarithm of", num, "is", log)
print("The sine of", num, "is", sin)
print("The cosine of", num, "is", cos)  
# Output
# The square root of 16 is 4.0
# The logarithm of 16 is 2.772588722239781
# The sine of 16 is -0.2879033166650653
# The cosine of 16 is -0.9576594803233847
# In this example, we imported the math module and used some of its functions to calculate the square root, logarithm, sine, and cosine of a number. The math module provides many mathematical functions that can be used in Python programs.